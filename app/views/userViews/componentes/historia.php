    <h3 class="text-center">HISTORIA</h3>
    <p>
        La Misión Sucre es una de las Misiones Bolivarianas creadas en Venezuela bajo el gobierno de Hugo Chávez. Fue establecida mediante el Decreto Presidencial N° 2.601 el 8 de septiembre de 20031. La misión lleva el nombre del Mariscal Antonio José de Sucre, un destacado líder militar y político venezolano.
    </p>
    <p>
    <h4> Objetivos de la Misión Sucre</h4>
    La Misión Sucre tiene como objetivo facilitar el acceso a la educación universitaria pública, permanente, integral, gratuita y en igualdad de oportunidades para todos los bachilleres que lo demanden. Su propósito es incrementar el nivel educativo de la población venezolana y formar ciudadanos comprometidos con el desarrollo del país1.
    </p>
    <p>
    <h4> Historia y Desarrollo</h4>
    Durante el gobierno de Hugo Chávez, se destinaron recursos significativos para fortalecer el sistema educativo venezolano. Esto incluyó la expansión de la matrícula en institutos y universidades nacionales, así como la creación de nuevas universidades como la Universidad Marítima del Caribe, la Universidad Nacional Experimental Politécnica de la Fuerza Armada Bolivariana (UNEFA), y la Universidad Bolivariana de Venezuela, entre otras1.
    </p>
    <p>
        La Misión Sucre se enfoca en ofrecer una educación universitaria de vanguardia, con pertinencia social y orientada a la transformación y aprovechamiento creativo de los conocimientos. Además, promueve el desarrollo local, regional y nacional a través de la participación y el ejercicio de la ciudadanía1.
    </p>
    <h4> Impacto</h4>
    <p>

        La Misión Sucre ha tenido un impacto significativo en la educación superior en Venezuela, proporcionando nuevas oportunidades de estudio para un gran número de bachilleres y respondiendo a la necesidad de transformar el sistema educativo superior en términos de cobertura geográfica y equilibrio territorial.
    </p>
